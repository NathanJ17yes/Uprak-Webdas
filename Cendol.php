        <h2>How to make</h2>
        <ol>
            <li>Campur semua bahan saus gula dan masak sampai mendidih. Kemudian angkat dan saring, lalu didinginkan.</li>
            <li>Masaklah saus santan, dengan mencampur seluruh bahan menjadi satu. Sambil terus diaduk perlahan hingga mendidih. Lalu angkat dan dinginkan.</li>
            <li>Masak cendol dengan mencampur tepung hunkwe, tepung beras, air, pasta pandan, dan garam. Aduk sampai merata sempurna dan adonan meletup-letup. Jika sudah matang, sisihkan.</li>
            <li>Siapkan air matang dalam baskom, kemudian diberi bongkahan es batu di dalamnya.</li>
            <li>Letakkan cetakan cendol di atas baskom berisi air, sehingga cendol bisa langsung masuk dalam baskom.</li>
            <li>Masukkan adonan cendol yang sudah matang tadi ke dalam cetakan cendol. Tekan pelan-pelan cetakannya menggunakan sendok.</li>
            <li>Tiriskan cendol.</li>
            <li>Masukkan cendol dalam gelas saji, kemudian tambahkan saus gula dan tuangkan santannya. Akan lebih segar dan nikmat lagi bila ditambahkan es batu.</li>
            <li>Es cendol dawet istimewa siap menyegarkan harimu.</li>


        </ol>
