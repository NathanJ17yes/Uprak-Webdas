        <h2>Cara Membuat</h2>
        <ol>
        <li>Siapkan wadah bersih untuk menguleni adonan.</li>    
        <li>Campurkan ikan tenggiri yang sudah dihaluskan, daun bawang, garam, dan gula pasir. Campur dengan menggunakan tangan atau spatula hingga tercampur rata.</li> 
        <li>Masukkan putih telur kemudian aduk.</li> 
        <li>Tuang sedikit demi sedikit air dingin sembari terus menguleni adonan.</li> 
        <li>Masukkan tepung sagu sedikit demi sedikit sembari terus diaduk, kemudian masukkan tepung sagu. Aduk adonan hingga rata.</li> 
        <li>Kukus tahu terlebih dahulu.</li> 
        <li>Iris bagian tengah tahu atau kosongkan bagian tengah tahu agar bisa diisi dengan adonan.</li> 
        <li>Masukkan adonan ke dalam tahu juga taruh adonan di tengah kulit pangsit. Tutup kulit pangsit. Sisihkan.</li> 
        <li>Kocok 2 butir telur dan beri sedikit garam.</li> 
        <li>Tahu dan kulit pangsit yang sudah diberi adonan kemudian masukkan ke dalam telur.</li> 
        <li>Siapkan penggorengan. Goreng batagor dalam minyak panas dengan nyala api sedang.</li> 
        <li>Goreng batagor hingga berwarna coklat keemasan atau matang.</li> 
        <li>Sajikan batagor dengan saus kacang, kecap, sambal, dan air perasan jeruk nipis.</li> 


        </ol>
